#include "minishell.h"

bool	ft_isempty(const char *str)
{
	while (*str != '\0')
	{
		if (!(*str >= '\t' && *str <= '\r') && *str != ' ')
			return (false);
		str++;
	}
	return (true);
}

bool	is_not_special_char(char c, char c_1)
{
	return (c != '\0' && c_1 != '\0' \
		&& c != '?' && c_1 != '@' && c_1 != '`'\
		&& !(c_1 >= '\t' && c_1 <= '\r') \
		&& !(c_1 >= ' ' && c_1 <= '/') \
		&& !(c_1 >= ':' && c_1 <= '>') \
		&& !(c_1 >= '[' && c_1 <= '^') \
		&& !(c_1 >= '{' && c_1 <= '~'));
}
